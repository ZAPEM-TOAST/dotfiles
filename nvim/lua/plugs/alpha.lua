    dashboard.section.header.val = pizzavim_ascii
    dashboard.section.buttons.val = {
        dashboard.button("f", " -> Find Ingredients", ":Telescope find_files <CR>"),
        dashboard.button("r", "󰊏 -> Fridge", ":Telescope oldfiles <CR>"),
        dashboard.button("p", " -> Projects", ":Telescope project <CR>"),
        dashboard.button("e", " -> New Pizza", ":ene <BAR> startinsert <CR>"),
        dashboard.button("c", " -> Kitchen", function()
            vim.cmd(":e " ..config_dir .. "/init.lua")
            vim.cmd(":Neotree dir=" .. config_dir)
        end),
        dashboard.button("q", " -> Quit PizzaVim", ":qa<CR>"),
    }

    local function footer()
        local stats = require('lazy').stats()
        local ms = (stats.startuptime * 100 + 0.5) / 100
        local foot = ' ' .. stats.count .. ' pizzas delivered in ' .. ms .. 'ms '
    return foot
        end


    dashboard.section.footer.val = footer()
    dashboard.section.footer.opts.hl = "Type"
    dashboard.section.header.opts.hl = "Include"
    dashboard.section.buttons.opts.hl = "Keyword"

    dashboard.opts.opts.noautocmd = true
    alpha.setup(dashboard.opts)
    end

return dashb




